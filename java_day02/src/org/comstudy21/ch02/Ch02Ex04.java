package org.comstudy21.ch02;

public class Ch02Ex04 {
	public static void main(String[] args) {
		System.out.printf("%c - %d\n", 'A', (int)'A');
		//System.out.printf("%c - %d\n", 'A', Integer.parseInt("65"));
		System.out.printf("%c - %d\n", 'a', (int)'a');
		System.out.printf("%c - %d\n", '0', (int)'0');
		System.out.printf("%c - %d\n", 'A'+1, (int)('A'+1));
		// %s=> 문자열   %c=> 문자   %d=> 숫자   %숫자=> 자릿수지정    %f=> float
		
	}
}
